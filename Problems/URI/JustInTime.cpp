int LargestPrime(int TheNum)
{
    int FactorCount = 0;

    for (int i = TheNum; i >= 2; --i)
    {

        for (int j = 2; j < sqrt(i) + 1; ++j) // HERE
        {
            if (i % j == 0)
            {
                ++FactorCount;
                break; // HERE
            }
        }

        if (FactorCount == 0)
        {
            return i;
            break;
        }

        FactorCount = 0;
    }
    return 2;
}

