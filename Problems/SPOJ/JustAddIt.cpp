const int MOD = 1e7 + 7;
int n, k;

ll binary_exp(ll a, ll b)
{
    a %= MOD;
    ll res = 1;
    while (b > 0)
    {
        if (b & 1)
            res = (res * a) % MOD;
        a = (a * a) % MOD;
        b >>= 1;
    }
    return res;
}

ll z(int n, int k)
{
    ll ans = 0;
    ans = (ans + 2 * binary_exp(n - 1, k)) % MOD;
    ans = (ans + 2 * binary_exp(n - 1, n - 1)) % MOD;
    ans = (ans + binary_exp(n, k)) % MOD;
    ans = (ans + binary_exp(n, n)) % MOD;
    return ans;
}

